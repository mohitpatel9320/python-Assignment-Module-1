# Demonstrating the creation of variables and different data types in Python

# Integer
age = 25
print("Integer:", age)

# Float
height = 5.9
print("Float:", height)

# String
name = "John Doe"
print("String:", name)

# Boolean
is_student = True
print("Boolean:", is_student)

# List
fruits = ["apple", "banana", "cherry"]
print("List:", fruits)

# Tuple
coordinates = (10.5, 20.3)
print("Tuple:", coordinates)

# Dictionary
person = {"name": "Alice", "age": 30, "city": "New York"}
print("Dictionary:", person)

# Set
unique_numbers = {1, 2, 3, 4, 5}
print("Set:", unique_numbers)

# NoneType
nothing = None
print("NoneType:", nothing)